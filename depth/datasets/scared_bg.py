class SCAREDDataset(Dataset):
    """
        ref: https://github.com/cogsys-tuebingen/mobilestereonet
    """
    def __init__(self, data_path, list_filename=None, is_train=True):
        self.data_path = data_path
        self.training = is_train

        if self.training:
            self.target_list = [
                'dataset_1', 'dataset_2', 'dataset_3',
                'dataset_6', 'dataset_7',
            ]
            # self.data_path = data_path + '/train'
        else:
            self.target_list = [
                'dataset_8', 'dataset_9',
            ]
            # self.data_path = data_path + '/test'

        self.data = {
            # 'img': [],
            'left_img': [],
            'right_img': [],
            'depth': [],
        }

        self.load_data()

    def load_data(self):
        for case_name in os.listdir(self.data_path):
            if case_name not in self.target_list:
                continue

            dpath = self.data_path + f'/{case_name}'

            for key_frame in os.listdir(dpath):
                base_path = dpath + f'/{key_frame}'
                img_dir_path = base_path + '/data/left_finalpass'
                img_dir_path2 = base_path + '/data/right_finalpass'
                depth_dir_path = base_path + '/data/disparity'
                # img_dir_path = base_path + '/frames_refine'
                # depth_dir_path = base_path + '/depth_map_refine'
                if os.path.exists(img_dir_path):
                # if len(os.listdir(img_dir_path)) > 0:
                    img_path_list = natsort.natsorted(glob(img_dir_path + '/*.png'))
                    img_path_list2 = natsort.natsorted(glob(img_dir_path2 + '/*.png'))
                    depth_path_list = natsort.natsorted(glob(depth_dir_path + '/*.tiff'))

                    # self.data['img'] += img_path_list
                    self.data['left_img'] += img_path_list
                    self.data['right_img'] += img_path_list2
                    self.data['depth'] += depth_path_list

    def load_image(self, filename):
        img = Image.open(filename).convert('RGB')
        

        return img

    def load_disp(self, filename):
        img = tiff.imread(filename)
        # img = np.nan_to_num(img, nan=0)
        img = np.ascontiguousarray(img, dtype=np.float32)

        return img

    def __len__(self):
        return len(self.data['depth'])

    def __getitem__(self, index):
        # left_filenmae = self.data['img'][index]#.split('/')[-1]
        # left_img, right_img = self.load_image(self.data['img'][index])
        left_filenmae = self.data['left_img'][index]
        left_img, right_img = self.load_image(self.data['left_img'][index]), self.load_image(self.data['right_img'][index])
        disparity = self.load_disp(self.data['depth'][index])
        org_left_img = left_img.copy()

        if self.training:
            w, h = left_img.size
            # crop_w, crop_h = 512, 256
            crop_w, crop_h = 960, 512

            x1 = random.randint(0, w - crop_w)
            y1 = random.randint(0, h - crop_h)

            # random crop
            left_img = left_img.crop((x1, y1, x1 + crop_w, y1 + crop_h))
            right_img = right_img.crop((x1, y1, x1 + crop_w, y1 + crop_h))
            disparity = disparity[y1:y1 + crop_h, x1:x1 + crop_w]

            # to tensor, normalize
            # processed = get_transform()
            processed = get_transform_train()
            left_img = processed(left_img)
            right_img = processed(right_img)

            return {"left": left_img,
                    "right": right_img,
                    "disparity": disparity}
        else:
            w, h = left_img.size
            # crop_w, crop_h = 960, 512
            crop_w, crop_h = w, h

            org_left_img = org_left_img.crop((w - crop_w, h - crop_h, w, h))
            left_img = left_img.crop((w - crop_w, h - crop_h, w, h))
            right_img = right_img.crop((w - crop_w, h - crop_h, w, h))
            disparity = disparity[h - crop_h:h, w - crop_w: w]

            processed = get_transform()
            left_img = processed(left_img)
            right_img = processed(right_img)

            return {"left": left_img,
                    #"right": right_img,
                    "depth_gt": disparity,
                    "top_pad": 0,
                    "right_pad": 0,
                    "left_filename": left_filenmae,
                    'org_left': np.array(org_left_img)}


